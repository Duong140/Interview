package com.Example.Utilities;

import java.time.LocalDateTime;



public class Util {
	
	
	
	
	public static String emailWithTimeStamp() {
		LocalDateTime date = LocalDateTime.now();
		String TimeStamp = date.toString().replaceAll("-", "_").replaceAll(":", "_");
		return "Seleniumpanda" + TimeStamp + "@gmail.com";
		
	}
	
	public static final int IMPLICIT_WAIT_TIME = 1000;
	public static final int PAGELOAD_TIME = 1000;
	public static final int SCRIPT_TIME = 1000;

}
